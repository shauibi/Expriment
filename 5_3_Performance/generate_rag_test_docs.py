# Each document: 20 paragraphs → ~20 chunks (one chunk per paragraph ≤ 512 chars)
PARAS_PER_DOC = 20

# Batch plan: batch_name → num_docs
BATCHES = [
    ("batch_01_100chunks",  5),   # 5×20=100 chunks → cumulative 100
    ("batch_02_250chunks",  8),   # 8×20=160 chunks → cumulative 260
    ("batch_03_400chunks",  8),   # 8×20=160 chunks → cumulative 420
    ("batch_04_600chunks", 10),   # 10×20=200 → cumulative 620
    ("batch_05_850chunks", 12),   # 12×20=240 → cumulative 860
    ("batch_06_1100chunks",13),   # 13×20=260 → cumulative 1120
    ("batch_07_1400chunks",15),   # 15×20=300 → cumulative 1420
    ("batch_08_1700chunks",15),   # 15×20=300 → cumulative 1720
    ("batch_09_2000chunks",15),   # 15×20=300 → cumulative 2020
]


def generate_doc(doc_index: int) -> str:
    """Generate ONE document with PARAS_PER_DOC paragraphs."""
    # Pick random paragraphs without replacement (if pool large enough)
    start = (doc_index * PARAS_PER_DOC) % len(PARAGRAPHS)
    selected = []
    for i in range(PARAS_PER_DOC):
        idx = (start + i) % len(PARAGRAPHS)
        selected.append(PARAGRAPHS[idx])
    # Shuffle so docs don't all look the same
    random.shuffle(selected)
    # Add paragraph separators (two newlines, as TextSplitter splits on empty line)
    return "\n\n".join(selected)


